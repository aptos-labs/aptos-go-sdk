// Package examples contains example programs demonstrating the Aptos Go SDK v2.
//
// Each subdirectory contains a standalone example that can be run with `go run`.
//
// # Examples
//
//   - transfer_coin: Basic APT transfer between accounts
//   - sponsored_transaction: Fee payer (sponsored) transactions
//   - fungible_asset: Working with fungible assets
//   - multi_agent: Multi-agent transaction signing
//   - keyless: Keyless (ZK) authentication
//
// # Running Examples
//
// Most examples connect to testnet and use the faucet:
//
//	cd examples/transfer_coin
//	go run main.go
//
// # Prerequisites
//
// Examples require:
//   - Go 1.23 or later
//   - Network access to Aptos testnet
package examples
